/// A library for handling Public DICOM Tags.
///
/// Compile-Time static constant definitions for Standard DICOM Data [Element] [Tag]s.
/// Each [Tag] contains the integer value of the associated Data [Element] definition
/// (see PS3.6).
///
/// Note: Some Data Elements [code]s that specify ranges do not have [int] values,
/// these [code]s are defined with string values.
//TODO: define a TagRange class for tags that have xx's in them
class Tag {
  //TODO: fix link
  // Standard Library definitions
  static const String docLink = 'doc/dicom/dart/tag.html';

  /// A bit mask, which when bitwise-anded (&) with [code] returns [tagGroup].
  static const int kGroupMask = 0xFFFF0000;

  /// A bit mask, which when bitwise-anded (&) with [code] returns [tagElement].
  static const int kElementMask = 0x0000FFFF;

  static bool allowInvalidPublicTags = false;
  static bool allowInvalidPrivateTags = false;
  static bool checkForDefinition = false;

  final int code;

  /// Returns one of:
  ///   - a canonical (public) DICOM [Tag],
  ///   - a [PrivateCreator] [Tag], which includes the private data elements, or
  ///   - if [allowInvalidTags] is [true] an [InvalidPrivateTag];
  ///   - otherwise, throws an [Error].
  factory Tag(int code) {
    if (tag.isPublic(code)) {
      var ded = ElementDef.lookup(code);
      if (ded != null) {
        return new Tag(code);
      } else if (allowInvalidPublicTags) {
        return new InvalidPublicTag(code);
      } else {
        throw "Invalid Public Tag: ${tag.hex(code)}";
      }
    } else if (tag.isPrivateCreator(code)) {
        return new PrivateCreator(code);
      } else if (allowInvalidPrivateTags) {
        return new InvalidPrivateTag(code);
      } else {
      throw "Invalid Tag: $code";
    }
  }

 /// Takes a [group] and [dictionary] and returns [tag].
  factory Tag.fromInts(int group, int element) => new Tag((group << 16) + element);

  const Tag._(this.code);

  /// Internal constructor used by subclasses.
  Tag.internal(this.code);

  /// Returns a hex [String] 8 characters long with a "0x" prefix.
  String get hex => intToHex(code);

  /// Returns [code] in DICOM format '(gggg,eeee)'.
  String get dcm => '($groupHex,$elementHex)';

  /// Returns the group number of [code].
  int get group => code >> 16;

  String get groupHex => intToHex(group, 4);

  int get elt => code & 0x0000FFFF;

  //int get element => code & kElementMask;

  String get elementHex => intToHex(elt, 4);



  //**** Predicates ****

  //TODO: this might be expensive
  bool get isValid => checkForDefinition && (deDef != null);

  /// Returns true if the tag is defined by DICOM, false otherwise
  /// All DICOM Public tags have group numbers that are even.
  /// Note: This only checks that the group number is even.
  bool get isPublic => true;

  //**** Private Tag Methods ****

  /// Returns [true] if [code] is a valid DICOM Private Tag.
  bool get isPrivate => false;

  /// Returns true if [code] is a valid [PrivateCreator] tag.
  bool get isPrivateCreator => false;

  bool get isPrivateData => false;

  /// Returns the Data Element Definition for this [Tag].
  ElementDef get deDef => ElementDef.lookup(code);

  String get keyword => deDef.keyword;

  String get name => deDef.name;

  VR get vr => deDef.vr;

  VM get vm => deDef.vm;

  int get minValues => vm.min;

  int get mixValues => vm.max;

  int get valuesWidth => vm.width;

  bool get isRetired => deDef.isRetired;


  //**** Tag Groups and Elements


  //TODO: move to Int
  static int intToGroup(int v) => v >> 16;

  static int intToElement(int v) => v & kElementMask;

  static String intToHex(int v, [int padding = 8]) =>
      "0x" + v.toRadixString(16).padLeft(padding, "0");



  //TODO:  move to Integer
  /// Returns a [List<String>], of decimal [String]s.
  static Iterable <String> intListToString(List<int> list) =>
      list.map((int i) => i.toRadixString(10));

//Flush
  /// Returns [true] if the [VR] of the [Tag] is [SQ].
//bool isSQTag(int tag) => tagToTagClassMap[tag].vr == VR.kSQ;

  bool isSQEnd(int tag) => tag == kSequenceDelimitationItem;

  //**** Item related

  /// The length of a [Sequence] or [Item] DelimitationItem field.
  static const int kDelimitationItemLength = 8;

  /// Returns [true] if [code] is  ItemTag
  bool get isItem => code == kItem;

  bool get isItemEnd => code == kItemDelimitationItem;

  // Tag Validators
  // Internal implementation of Tag Validators
  Exception rangeError(Tag min, Tag max) {
    String msg = 'invalid tag: $code not inRange of $min <= x <= $max';
    throw new RangeError(msg);
  }

  bool checkRange(test(Tag min, Tag max), Tag min, Tag max) =>
      test(min, max) ? true : rangeError(min, max);

  /// True for any Tag in the DICOM range, false otherwise.
  /// The minimum legal tag value.
  bool get inRange =>
    (kMinTag <= code) && (code <= kMaxTag);

  /// [True] for any File Meta Information [Tag], false otherwise.
  /// Note: Does not test flag validity
  bool get isFmiTag =>
      (kMinFmiTag <= code) && (code <= kMaxFmiTag);

  /// Returns [true] if [code] is in the range of DICOM Directory Tags
  bool get isDcmDir =>
      (kMinDcmDirTag <= code) && (code <= kMaxDcmDirTag);


  /// [True] for any [Tag] that can be contained in a general [Dataset], false otherwise.
  bool get inDataset =>
    (kMinDatasetTag <= code) && (code <= kMaxDatasetTag);

  String toString() => 'Tag: $hex';

  //**** Static Methods ****

  /// Return an [int] if the [String] can be parsed as hexadecimal or [null].
  /// The [String may be of the form "(gggg,eeee)" and may have leading or
  /// trailing spaces.
  static int toInt(String s) {
    s = s.trim();
    s = s.replaceAll('(),', "");
    return int.parse(s, radix: 16);
  }
  /// Returns the [Tag] corresponding the [value], or [null];
  static Tag lookup(int code) => (deDefs[code] == null) ? new Tag(code) : null;

  /// Returns a [Tag] if [s] is valid, otherwise [null]:
  static Tag parse(String s) {
    int code = int.parse(s);
    if (tag.isPublic(code)) {
      return new Tag(code);
    } else if (tag.isPrivateCreator(code)) {
      return new PrivateCreator(code);
    } 
    return null;
  }

  /// Parses a DICOM format tag.  Format is "(gggg,eeee)".
  static Tag parseDcm(String s) =>
    parse(s.substring(1, 5) + s.substring(6, 10));

  /// Returns a [List] of DICOM tag codes in '0xggggeeee format
  static List<String> listToHex(List<Tag> list) => list.map((Tag t) => t.hex);

  /// Returns a [List] of DICOM tag codes in '(gggg,eeee)' format
  static Iterable<String> listToDcm(List<Tag> list) => list.map((Tag t) => t.dcm);


