class SequenceTag {

  List<int> tagList;

  SequenceTag(List<int> tagList) : tagList = (isValid(tagList)) ? tagList : null;

  int operator [](int i) => tagList[i];

  int get length => tagList.length;

  int get last => tagList.last;

  static bool isValid(List<int> tagList) {
    for(int i = 0; i < (tagList.length - 1); i++) {
      int tag = tagList[i];
      if (ElementDef.lookup(tag).vr != VR.kSQ) return false;
    }

    return (ElementDef.lookup(tagList.last) == null) ? false : true;
  }

}

