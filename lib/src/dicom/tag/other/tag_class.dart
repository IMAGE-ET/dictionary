/// DICOM Private Creator Tag
class PrivateCreator extends PrivateTag {

  factory PrivateCreator(int code) =>
      (tag.isPrivateCreator(code)) ? new PrivateCreator._(code) : null;

  PrivateCreator._(int code) : super._(code);

  bool get isCreator => true;

  int get index => code & 0xFF;

  /// The minimum  private data tag
  int get base => index << 8;

  /// The dictionary value of the maximum private data tag
  int get limit => base + 0xFF;

  /// [True] if [v] is a valid private data dictionary for this creator.
  static bool inPrivateCreator(int v) => tag.isPrivateCreator(v);

